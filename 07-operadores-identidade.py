#Operadores de identidade (demonstração)
x = 3	
y = -10.5 
z = 15*23j #23j é um número complexo
pais = "Brasil"	

print("x =", x, "y =", y)
print("z =", z, " pais =", pais)
print("x is 3:", x is 3)
print("y is 7.5:",	y is 7.5)
print("x is not 5:", x is not 5)
	
#Mostrando os tipos de todas as variáveis
print("type(x):", type(x))
print("type(y):", type(y))
print("type(z):", type(z))
print("type(pais):", type(pais))
	
#Verificando se uma varíavel é de um certo tipo
print("type(x) is	int:", type(x) is int)
print("type(y) is	int:", type(y) is int)
print("type(z) is	complex", type(z) is complex)
print("type(pais)	is not float:", type(pais) is not int)

